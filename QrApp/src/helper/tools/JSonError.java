package helper.tools;

public class JSonError {
	int status;
	String msg;
	String data;
	String activity;
	
	public int getStatus(){
		return this.status;
	}
	
	public String getMsg(){
		return this.msg;
	}
	
	public String getData(){
		return this.data;
	}
	
	public String getAct(){
		return this.activity;
	}
}
