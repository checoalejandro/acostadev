package helper.json;

public class User {
	
	int id;
	String name;
	String username;
	String email;
	String url_image;
	String password;
	
	public int getUserId(){
		return id;
	}
	
	public String getName(){
		return name;
	}
	
	public String getUserName(){
		return username;
	}
	
	public String getEmail(){
		return email;
	}
	
	public String getUrlImage(){
		return url_image;
	}
	
	public String getPassword(){
		return password;
	}

}
