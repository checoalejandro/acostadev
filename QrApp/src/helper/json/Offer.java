package helper.json;

public class Offer {

	String name;
	String start;
	String finish;
	int groupid;
	int offerid;
	
	public String getName(){
		return this.name;
	}
	
	public String getStartDate(){
		return this.start;
	}
	
	public String getFinishDate(){
		return this.finish;
	}
	
	public int getGroupId(){
		return this.groupid;
	}
	
	public int getOfferId(){
		return this.offerid;
	}
}
